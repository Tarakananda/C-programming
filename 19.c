#include <stdio.h>                      //switch case example
int main()
{
    int i;
    scanf("%d",&i);
    switch (i)
    {
    case 1:
        printf("tharak nandha\n");
        break;
    case 2:
        printf("lohitha \n");
        break;
    case 3:
        printf("bharath kumar\n");
        break;
    case 4:
        printf("vasundhara\n");
        break;
    default:
        printf("pulla reddy\n");
        break;
    }
}